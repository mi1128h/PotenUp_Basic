#include <iostream>

/*
    템플릿: 다양한 자료형에 대해 일반화된 코드(Generic)를 작성할 수 있게 하는 문법
*/

// 함수 템플릿
template<typename T>
T Add(T a, T b) { return a + b; }

// 팩토리얼
template<int N>
struct Factorial {
    static const int value = N * Factorial<N - 1>::value;
};

template<>
struct Factorial<0> {
    static const int value = 1;
};

int main()
{
    std::cout << Factorial<5>::value << std::endl;
    std::cout << Add(5, 3) << std::endl;
    std::cout << Add(5.7f, 3.9f) << std::endl;
    std::cout << (int)Add('a', 'c') << std::endl;
}

